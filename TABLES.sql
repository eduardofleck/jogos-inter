DROP table IF EXISTS game;
DROP table IF EXISTS round;
DROP table IF EXISTS championship;
DROP table IF EXISTS team;
DROP table IF EXISTS stadium;

CREATE TABLE stadium(
    id INT IDENTITY,
    name varchar(300) not null,
	nickname varchar(300) null,
	countrycode varchar(2) null,
	city varchar(300) null,
    createddate date not null default GETDATE(),
	PRIMARY KEY(id)
);

CREATE TABLE team(
    id INT IDENTITY,
    name varchar(300) not null,
	size varchar null,
    createddate date not null default GETDATE(),
	PRIMARY KEY(id)
);

CREATE TABLE championship(
    id varchar(15) not null,
    name varchar(300) not null,
    createddate date not null default GETDATE(),
	PRIMARY KEY(id)
);

CREATE TABLE round(
    id INT IDENTITY,
    round varchar(50) not null,
	createddate date not null default GETDATE(),
	PRIMARY KEY(id)
);

CREATE TABLE game(
    id INT IDENTITY,
	idChampionship varchar(15) not null,
    idRound int null,
	idStadium int null,
	attendance int null,
	idTeamHome int not null,
	scoreTeamHome int null,
	penaltiesScoreTeamHome int null,
	idTeamAway int not null,
	scoreTeamAway int null,
	penaltiesScoreTeamAway int null,
	gameDate date null,
	gameCanceled bit default 0, 
	createddate date not null default GETDATE(),
	source varchar(50) not null,
	UNIQUE (idChampionship, idTeamHome, idTeamAway, gameDate),
	UNIQUE (idTeamHome, idTeamAway, gameDate),
	PRIMARY KEY(id),
	CONSTRAINT fk_game_championship FOREIGN KEY (idChampionship) REFERENCES championship (id),
	CONSTRAINT fk_game_round FOREIGN KEY (idRound) REFERENCES round (id),
	CONSTRAINT fk_game_stadium FOREIGN KEY (idStadium) REFERENCES stadium (id),
	CONSTRAINT fk_game_teamHome FOREIGN KEY (idTeamHome) REFERENCES team (id),
	CONSTRAINT fk_game_teamAway FOREIGN KEY (idTeamAway) REFERENCES team (id)
);

